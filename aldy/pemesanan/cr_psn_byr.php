Cara Pembelian

    Pada halaman home, pilih salah satu desain baju kaos, klik beli
    Lalu pilih ukuran yang ingin dipilih, klik tombol [lanjut]
    Lalu anda akan masuk ke halaman keranjang, untuk mengubah jumlah pembelian, tinggal isi jumlah yang akan dibeli, lalu klik update
    Jika ingin mendelete, klik gambar silang yang ada di sebelah kanan
    Jika sudah selesai milih-milih bajunya, isi form data pembeli dan alamat tujuan pengiriman
    Klik tombol [submit]
    Anda telah selesai belanja, lanjutkan dengan pembayaran 

